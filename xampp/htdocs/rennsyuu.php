<?php

//文字の表示 「こんにちは」
print"こんにちは<br>";

//引数の表示（円周率）　「3.1415926535898」
print pi();
echo "<br>";

//乱数　０～６の数字を不規則に出す（F5キーで表示更新）「１」「２」「３」「４」「５」「６」
print rand(1,6);
echo "<br>";

//バックグラウンドの色変更
print '<BODY BGCOLOR="yellow">';

//ヒアドキュメント利用で指定のURLへ移動　「ソフトクリエイティブへ」
//「<<<終了をあらわす文字」の次から「終了をあらわす文字;」までの範囲を一連の文字列として扱う。
print <<<owari
<A HREF = "http://www.sbcr.jp">ソフトクリエイティブへ</A>
owari;
echo "<br>";

//変数の表示　「2015」
$a = 2015;
print "$a";
echo "<br>";

//変数の組み合わせで文字の表示(文章)　「「基礎」からの知識を重視したPHPの参考書です。」
$a = "基礎";
$b = "からの";
$c = "PHP";
print "「{$a}」{$b}知識を重視した{$c}の参考書です。";
echo "<br>";

//変数の組み合わせで文字の表示(変数のみ)　「基礎からのPHP」
print $a.$b.$c;
echo "<br>";

//変数の組み合わせで文字の表示(変数のみ)　「基礎からのPHP」
//「. =」は右側の文字列を変数の値に連結する
$a = "基礎";
$a = $a . "からの";
$a = $a . "PHP";
print $a;

//文字に改行を入れる　「①基礎」
//                  「②からの」
//                  「③PHP」
//「nl2br」は、("")内にある文字を改行して表示する
